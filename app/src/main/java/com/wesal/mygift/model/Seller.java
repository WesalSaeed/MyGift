package com.wesal.mygift.model;

public class Seller {
    private String sellerIconUrl;
    private String sellerIconName;
    private String sellerId;
    private String sellerFullName;
    private String sellerEmail;
    private String sellerPhone;
    private String sellerBussName;
    private String sellerBussType;


    public String getSellerIconUrl() {
        return sellerIconUrl;
    }

    public void setSellerIconUrl(String sellerIconUrl) {
        this.sellerIconUrl = sellerIconUrl;
    }

    public String getSellerIconName() {
        return sellerIconName;
    }

    public void setSellerIconName(String sellerIconName) {
        this.sellerIconName = sellerIconName;
    }

    public String getSellerId() {
        return sellerId;
    }

    public void setSellerId(String sellerId) {
        this.sellerId = sellerId;
    }

    public String getSellerFullName() {
        return sellerFullName;
    }

    public void setSellerFullName(String sellerFullName) {
        this.sellerFullName = sellerFullName;
    }

    public String getSellerEmail() {
        return sellerEmail;
    }

    public void setSellerEmail(String sellerEmail) {
        this.sellerEmail = sellerEmail;
    }

    public String getSellerPhone() {
        return sellerPhone;
    }

    public void setSellerPhone(String sellerPhone) {
        this.sellerPhone = sellerPhone;
    }

    public String getSellerBussName() {
        return sellerBussName;
    }

    public void setSellerBussName(String sellerBussName) {
        this.sellerBussName = sellerBussName;
    }

    public String getSellerBussType() {
        return sellerBussType;
    }

    public void setSellerBussType(String sellerBussType) {
        this.sellerBussType = sellerBussType;
    }
}
